#include <stdio.h>
#include <windows.h>
#include <stdlib.h>

int main() { // Объявляем главную функцию
    SetConsoleOutputCP(CP_UTF8); // Устанавливаем кодировку UTF-8 для вывода русских символов

    int a, b, c, d;
    int diff1, diff2, diff3;

    printf("Введите четыре числа (a b c d): ");
    scanf("%d %d %d %d", &a, &b, &c, &d);

    // Вычисляем разности между последовательными числами
    diff1 = b - a;
    diff2 = c - b;
    diff3 = d - c;

    // Проверяем, образуют ли числа арифметическую прогрессию
    if (diff1 == diff2 && diff2 == diff3) {
        printf("Числа образуют арифметическую прогрессию.\n");
    } else {
        printf("Числа не образуют арифметическую прогрессию.\n");
    }

    return 0;
}